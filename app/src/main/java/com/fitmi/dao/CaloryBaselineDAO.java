package com.fitmi.dao;

public class CaloryBaselineDAO {
	
	private String userProfileId = "";
	private String userId = "";
	private String totalIntake = "";
	private String totalBurned = "";
	private String weight = "";
	private String sleep = "";
	private String water = "";
	private String bpSys = "";
	private String bpDia = "";
	
	
	public String getUserProfileId() {
		return userProfileId;
	}
	public void setUserProfileId(String userProfileId) {
		this.userProfileId = userProfileId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getTotalIntake() {
		return totalIntake;
	}
	public void setTotalIntake(String totalIntake) {
		this.totalIntake = totalIntake;
	}
	public String getTotalBurned() {
		return totalBurned;
	}
	public void setTotalBurned(String totalBurned) {
		this.totalBurned = totalBurned;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getSleep() {
		return sleep;
	}
	public void setSleep(String sleep) {
		this.sleep = sleep;
	}
	public String getWater() {
		return water;
	}
	public void setWater(String water) {
		this.water = water;
	}
	public String getBpSys() {
		return bpSys;
	}
	public void setBpSys(String bpSys) {
		this.bpSys = bpSys;
	}
	public String getBpDia() {
		return bpDia;
	}
	public void setBpDia(String bpDia) {
		this.bpDia = bpDia;
	}
	
}
